	public Vision(){
		visionThread = new Thread(() -> {
			// Get the UsbCamera from CameraServer
			// Set the resolution
			camera = CameraServer.getInstance().startAutomaticCapture();

			camera.setResolution(640, 480);

			// Get a CvSink. This will capture Mats from the camera
			CvSink cvSink = CameraServer.getInstance().getVideo();
			// Setup a CvSource. This will send images back to the Dashboard
			outputStream = CameraServer.getInstance().putVideo("Rectangle", 640, 480);
			// Mats are very memory expensive. Lets reuse this Mat.
			Mat mat = new Mat();

			// This cannot be 'true'. The program will never exit if it is. This
			// lets the robot stop this thread when restarting robot code or
			// deploying.
			while (!Thread.interrupted()) {
				// Tell the CvSink to grab a frame from the camera and put it
				// in the source mat.  If there is an error notify the output.
				if (cvSink.grabFrame(mat) == 0) {
					// Send the output the error.
					outputStream.notifyError(cvSink.getError());
					// skip the rest of the current iteration
					continue;
				}
				// Put a rectangle on the image
				Imgproc.rectangle(mat, new Point(100, 100), new Point(400, 400),
						new Scalar(255, 255, 255), 5);
				// Give the output stream a new image to display
				outputStream.putFrame(mat);
//				process(mat);
			}
		});
		visionThread.setDaemon(true);
		visionThread.start();

	}
	public void end(){
		visionThread.interrupt();
	}
	public double[][] visionOutput(){
		if(!convexHullsOutput.isEmpty()){
			data = new double[convexHullsOutput.size()][3];
			for( int i = 0; i < convexHullsOutput.size();i++){
				Rect r = Imgproc.boundingRect(convexHullsOutput.get(i));
				synchronized (imgLock) {
					data[i][0] = r.x + (r.width / 2);
					data[i][1] =  r.y + (r.height / 2);
					data[i][2] = r.height*r.width;
				}
			}
		}
		return data;
	}
	public double[][] largestContours(int numReturn){
		largConts = new double[numReturn][3];
		temp = new double[convexHullsOutput.size()][3];
		temp = visionOutput();
		for(int i = 0; i < Math.max(numReturn, temp.length); i++){
			largConts[i][2] 
					= temp[i][2];
			for(int p = 0; p < convexHullsOutput.size(); p++){
				if(temp[p][2] >= largConts[i][2]){
					largConts[i] = temp[p];
					temp[p][0] =-1;
					temp[p][1]=-1;
					temp[p][2]=-1;
				}
			}
		}

		return null;
	}
	public void process(Mat source0) {
		// Step HSV_Threshold0:
		Mat hsvThresholdInput = source0;
		double[] hsvThresholdHue = {32.37410071942446, 98.60068259385666};
		double[] hsvThresholdSaturation = {0.0, 113.5750853242321};
		double[] hsvThresholdValue = {248.12050359712228, 255.0};
		hsvThreshold(hsvThresholdInput, hsvThresholdHue, hsvThresholdSaturation, hsvThresholdValue, hsvThresholdOutput);

		// Step HSL_Threshold0:
		Mat hslThresholdInput = source0;
		double[] hslThresholdHue = {67.79661016949153, 101.39037433155079};
		double[] hslThresholdSaturation = {50.42372881355933, 255.0};
		double[] hslThresholdLuminance = {0.0, 255.0};
		hslThreshold(hslThresholdInput, hslThresholdHue, hslThresholdSaturation, hslThresholdLuminance, hslThresholdOutput);

		// Step CV_bitwise_or0:
		Mat cvBitwiseOrSrc1 = hsvThresholdOutput;
		Mat cvBitwiseOrSrc2 = hslThresholdOutput;
		cvBitwiseOr(cvBitwiseOrSrc1, cvBitwiseOrSrc2, cvBitwiseOrOutput);

		// Step CV_erode0:
		Mat cvErodeSrc = cvBitwiseOrOutput;
		Mat cvErodeKernel = new Mat();
		Point cvErodeAnchor = new Point(-1, -1);
		double cvErodeIterations = 3.0;
		int cvErodeBordertype = Core.BORDER_CONSTANT;
		Scalar cvErodeBordervalue = new Scalar(-1);
		cvErode(cvErodeSrc, cvErodeKernel, cvErodeAnchor, cvErodeIterations, cvErodeBordertype, cvErodeBordervalue, cvErodeOutput);

		// Step Find_Contours0:
		Mat findContoursInput = cvErodeOutput;
		boolean findContoursExternalOnly = false;
		findContours(findContoursInput, findContoursExternalOnly, findContoursOutput);

		// Step Filter_Contours0:
		ArrayList<MatOfPoint> filterContoursContours = findContoursOutput;
		double filterContoursMinArea = 200.0;
		double filterContoursMinPerimeter = 0.0;
		double filterContoursMinWidth = 0.0;
		double filterContoursMaxWidth = 1000.0;
		double filterContoursMinHeight = 0.0;
		double filterContoursMaxHeight = 1000.0;
		double[] filterContoursSolidity = {0, 100};
		double filterContoursMaxVertices = 1000000.0;
		double filterContoursMinVertices = 0.0;
		double filterContoursMinRatio = 0.0;
		double filterContoursMaxRatio = 1000.0;
		filterContours(filterContoursContours, filterContoursMinArea, filterContoursMinPerimeter, filterContoursMinWidth, filterContoursMaxWidth, filterContoursMinHeight, filterContoursMaxHeight, filterContoursSolidity, filterContoursMaxVertices, filterContoursMinVertices, filterContoursMinRatio, filterContoursMaxRatio, filterContoursOutput);

		// Step Convex_Hulls0:
		ArrayList<MatOfPoint> convexHullsContours = filterContoursOutput;
		convexHulls(convexHullsContours, convexHullsOutput);

	}

